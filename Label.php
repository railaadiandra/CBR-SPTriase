  <p>Pembagian Label triase </p>
  <p><span ><br />
    1. (Merah)
Pasien dapat diberi label/warna merah jika mengalami kondisi kritis yang memerlukan pertolongan segera <br />
    <br />
    2. (Kuning)
Pasien dengan label/warna kuning menduduki prioritas kedua dalam system triase. Pasien yang masuk dalam kategori ini berada dalam kondisi yang tidak kritis, namun membutuhkan pertolongan segera.
    </h1>
    <br />
     
    <br />
    3.</b> (Hijau)
Pasien dengan prioritas ini diberi label/warna hijau menduduki prioritas ketiga dimana pasien pada label ini membutuhkan pertolongan tapi tidak urgent<br />
    <br />
    4.</b> (Hitam)
Label/warna ini hanya diperuntukkan bagi pasien yang tidak perlu penanganan segera </span></p>
